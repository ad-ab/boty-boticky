<p>
  Správce prohlašuje, že přijal veškerá vhodná technická a organizační opatření
  k zabezpečení osobních údajů. Správce přijal technická opatření k zabezpečení
  datových úložišť a úložišť osobních údajů v listinné podobě, zejména
  zaheslování počítačů, zaheslování jednotlivých administrativních a účetních
  programů, zabezpečené místnosti pro uložení dokumentů. Správce prohlašuje, že
  k osobním údajům mají přístup pouze jím pověřené osoby.
</p>

