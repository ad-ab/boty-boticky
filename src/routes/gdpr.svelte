<h3>II. Zdroje a kategorie zpracovávaných osobních údajů</h3>
<p>
  Správce zpracovává osobní údaje, které jste mu poskytl/a nebo osobní údaje,
  které správce získal na základě plnění Vaší objednávky. Správce zpracovává
  Vaše identifikační a kontaktní údaje a údaje nezbytné pro plněné smlouvy
</p>

<h3>III. Zákonný důvod a účel zpracování osobních údajů</h3>
<p>
  Zákonným důvodem zpracování osobních údajů je: plnění smlouvy mezi Vámi a
  správcem podle čl. 6 odst. 1 písm. b) GDPR a oprávněný zájem správce na
  poskytování přímého marketingu (zejména pro zasílání obchodních sdělení a
  newsletterů) podle čl. 6 odst. 1 písm. f) GDPR
</p>
<p>
  Účelem zpracování osobních údajů je :vyřízení Vaší objednávky a výkon práv a
  povinností vyplývajících ze smluvního vztahu mezi Vámi a správcem; při
  objednávce jsou vyžadovány osobní údaje, které jsou nutné pro úspěšné vyřízení
  objednávky (jméno a adresa, kontakt), poskytnutí osobních údajů je nutným
  požadavkem pro uzavření a plnění smlouvy, bez poskytnutí osobních údajů není
  možné smlouvu uzavřít či jí ze strany správce plnit a zasílání obchodních
  sdělení a činění dalších marketingových aktivit. Ze strany správce nedochází k
  automatickému individuálnímu rozhodování ve smyslu čl. 22 GDPR. S takovým
  zpracováním jste poskytl/a svůj výslovný souhlas.
</p>

<h3>IV. Doba uchovávání údajů</h3>
<p>
  Správce uchovává osobní údaje po dobu nezbytnou k výkonu práv a povinností
  vyplývajících ze smluvního vztahu mezi Vámi a správcem a uplatňování nároků z
  těchto smluvních vztahů (po dobu 15 let od ukončení smluvního vztahu). Po
  uplynutí doby uchovávání osobních údajů správce osobní údaje vymaže.
</p>

<h3>V. Příjemci osobních údajů (subdodavatelé správce)</h3>
<p>
  Příjemci osobních údajů jsou osoby podílející se na dodání zboží / služeb /
  realizaci plateb na základě smlouvy, zajišťující služby provozování e-shopu a
  další služby v souvislosti s provozováním e-shopu, zajišťující marketingové
  služby na základě smlouvy zajišťující účetní služby na základě smlouvy.
  Správce nemá v úmyslu předat osobní údaje do třetí země (do země mimo EU) nebo
  mezinárodní organizaci. Příjemci osobních údajů ve třetích zemích jsou
  poskytovatelé mailingových služeb / cloudových služeb
</p>

<h3>VI. Vaše práva</h3>

<p>
  Za podmínek stanovených v GDPR máte právo na přístup ke svým osobním údajům
  dle čl. 15 GDPR, právo opravu osobních údajů dle čl. 16 GDPR, popřípadě
  omezení zpracování dle čl. 18 GDPR. právo na výmaz osobních údajů dle čl. 17
  GDPR, právo vznést námitku proti zpracování dle čl. 21 GDPR a právo na
  přenositelnost údajů dle čl. 20 GDPR. právo odvolat souhlas se zpracováním
  písemně nebo elektronicky na adresu nebo email správce uvedený v čl. III
  těchto podmínek. Dále máte právo podat stížnost u Úřadu pro ochranu osobních
  údajů v případě, že se domníváte, že bylo porušeno Vaší právo na ochranu
  osobních údajů.
</p>

