Internetový prodej má jednu zásadní nevýhodu a tou je, že si Vaše dítě obuv nemůže vyzkoušet. Proto jsme pro Vás připravili rady pro výběr obuvi, které Vám pomohou s výběrem správné velikosti. Protože se udávaná velikost může u různých výrobců lišit, doporučujeme vždy před zakoupením nových botiček přeměřit velikost chodidla.

Nejdůležitější při výběru dětské obuvi je správně vybrat její velikost, která zaručuje noze zdravý vývoj a růst. Dětská noha roste, ale obuv nikoliv. Pro všechny rodiče je jistě velmi důležité pravidelně kontrolovat, zda dítě z obuvi nevyrostlo. U předškoláků a mladších školáků jednou za 2 až 3 měsíce, u starších školáků po 4 až 5 měsících. Pro správné a pohodlné měření existuje několik metod, avšak po našich zkušenostech doporučujeme následující:
Postavte dítě v tenkých ponožkách nebo bosou nohou na papír a tužkou mu obkreslete obě nohy. Tužku držte kolmo k podložce, aby nedošlo ke zkreslení obrysu nohy. Obkreslete obě nohy, protože téměř každý člověk má jednu nohu delší a pro správné určení velikosti dětské obuvi je třeba vycházet z té delší.
Označte si střed paty a střed nejdelšího prstu a změřte vzdálenost těchto dvou bodů.
Pro zjištění potřebné velikosti dětské obuvi je ke změřené délce ještě navíc potřeba připočítat takzvaný nadměrek v délce 10-12 mm pro letní a celoroční obuv. U zimní obuvi může být nadměrek až 15 mm
Po přičtení nadměrku zjistíte, jaký rozměr má mít vnitřní část obuvi.
Pokud si i přesto nejste jisti s výběrem správné velikosti, můžete nás kontaktovat a my Vám rádi poradíme.
Dětskou obuv prodáváme již několik let a za tu dobu jsme získali mnoho zkušeností. Snažíme se Vám výběr dětské obuvi zjednodušit a pomoci Vám vybrat tu správnou velikost. U každého modelu uvádíme velikost od výrobce a vnitřní rozměr, který sami důkladně měříme.

 
